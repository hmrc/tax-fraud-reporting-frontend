package forms

import forms.behaviours.StringFieldBehaviours
import play.api.data.FormError

class YourContactDetailsFormProviderSpec extends StringFieldBehaviours {

  val form = new YourContactDetailsFormProvider()()

  ".firstName" - {

    val fieldName = "firstName"
    val requiredKey = "yourContactDetails.error.firstName.required"
    val lengthKey = "yourContactDetails.error.firstName.length"
    val maxLength = 255

    behave like fieldThatBindsValidData(
      form,
      fieldName,
      stringsWithMaxLength(maxLength)
    )

    behave like fieldWithMaxLength(
      form,
      fieldName,
      maxLength = maxLength,
      lengthError = FormError(fieldName, lengthKey, Seq(maxLength))
    )

    behave like mandatoryField(
      form,
      fieldName,
      requiredError = FormError(fieldName, requiredKey)
    )
